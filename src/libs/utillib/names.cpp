#include "exportcontext.h"
#include "names.h"
#include "logging.h"

namespace qblocks {

//-----------------------------------------------------------------------
bool forEveryName(NAMEITEMFUNC func, void* data) {
    if (!func)
        return false;

    for (auto name : expContext().namesMap)
        if (!(*func)(name, data))
            return false;
    return true;
}

}  // namespace qblocks
